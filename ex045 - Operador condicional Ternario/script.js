// Operador condicional ternário (Ternario)

// Dependendo da condição, nos receberemos
// valores diferentes

// Condição então valor 1 se não valor 2
// condition? value1 : value2

// EXEMPLOS
// Café da manhã top
let pao = true
let queijo = true

const niceBreakfast = pao && queijo ? 'Café top' : 'Café ruim'

